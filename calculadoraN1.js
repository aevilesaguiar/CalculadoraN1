function adicionar(n1,n2){

    return n1+n2;
}
console.log(adicionar(10,20));

function subtracao(n1,n2){

    return n1-n2;
}
console.log(subtracao(30,20));


function divisao(n1,n2){

    return n1/n2;
}
console.log(divisao(30,20));

function multiplicacao(n1,n2){

    return n1*n2;
}
console.log(multiplicacao(30,20));


